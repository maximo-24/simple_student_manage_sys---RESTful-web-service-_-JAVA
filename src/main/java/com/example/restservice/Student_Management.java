package com.example.restservice;

public class Student_Management {
	private String studNumber;
	private String studName;
	private String studCourse;
	private int studYear;
	private char studSection;
	private int studAge;
	private String studAddress;
	
	public String getStudNumber() {
		return studNumber;
	}
	
	public void setStudNumber(String studNumber) {
		this.studNumber = studNumber;
	}
	
	public String getStudName() {
		return studName;
	}
	
	public void setStudName(String studName) {
		this.studName = studName;
	}
	
	public String getStudCourse() {
		return studCourse;
	}
	
	public void setStudCourse(String studCourse) {
		this.studCourse = studCourse;
	}
	
	public int getStudYear() {
		return studYear;
	}
	
	public void setStudYear(int studYear) {
		this.studYear = studYear;
	}
	
	public char getStudSection() {
		return studSection;
	}
	
	public void setStudSection(char studSection) {
		this.studSection = studSection;
	}
	
	public int getStudAge() {
		return studAge;
	}
	
	public void setStudAge(int studAge) {
		this.studAge = studAge;
	}
	
	public String getStudAddress() {
		return studAddress;
	}
	
	public void setStudAddress(String studAddress) {
		this.studAddress = studAddress;
	}
}
